// Simple if statement

let a = 10;

if (a === 10) {
    console.log("a is 10")
}

// If with else [else is default statement]

if (a === 10) {
    console.log("a is 10")
}
else {
    console.log("a doesn't have the expected value")
}

// if, else if and else

if (a === 10) {
    console.log("a is 10")
}
else if (a === 15) {
    console.log("a is 15")
}
else {
    console.log("a doesn't have the expected value")
}

// if, multiple else if and else

if (a === 10) {
    console.log("a is 10")
}
else if (a === 15) {
    console.log("a is 15")
}
else if (a === 20) {
    console.log("a is 15")
}
else if (a === 25) {
    console.log("a is 15")
}
else {
    console.log("a doesn't have the expected value")
}