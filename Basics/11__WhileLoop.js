// While loop - entry check loop

let n = 6;

while (n <= 8) {

    if (n === 7) {

        console.log(`The value of n is ${n}`)
        break
    }
    
    n = n + 1
}


// Do while loop - exist check loop

let k = 1;

do {
    console.log(`The value of k is ${k}`)
    k = k + 1
} while(k < 0)


